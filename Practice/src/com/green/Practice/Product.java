package com.green.Practice;

public class Product {
	private String proName;
	private String proCode;
	private String proPrice;
	private String proMaker;
	private String proMaterial;
	private String proSold;
	
	public String getProName() {
		return proName;
	}
	public void setProName(String proName) {
		this.proName = proName;
	}
	public String getProCode() {
		return proCode;
	}
	public void setProCode(String proCode) {
		this.proCode = proCode;
	}
	public String getProPrice() {
		return proPrice;
	}
	public void setProPrice(String proPrice) {
		this.proPrice = proPrice;
	}
	public String getProMaker() {
		return proMaker;
	}
	public void setProMaker(String proMaker) {
		this.proMaker = proMaker;
	}
	public String getProMaterial() {
		return proMaterial;
	}
	public void setProMaterial(String proMaterial) {
		this.proMaterial = proMaterial;
	}
	public String getProSold() {
		return proSold;
	}
	public void setProSold(String proSold) {
		this.proSold = proSold;
	}
	
	
	
}
